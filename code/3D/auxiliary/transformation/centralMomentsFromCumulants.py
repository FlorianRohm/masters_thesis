from numpy import *

def centralMomentsFromCumulants( rhoInv , C_000, C_001, C_002, C_010, C_011, C_012, C_020, C_021, C_022,
    C_100, C_101, C_102, C_110, C_111, C_112, C_120, C_121, C_122,
    C_200, C_201, C_202, C_210, C_211, C_212, C_220, C_221, C_222):


    k_211 = C_211 + (C_200*C_011 + 2*C_110*C_101)*rhoInv
    k_121 = C_121 + (C_020*C_101 + 2*C_011*C_110)*rhoInv
    k_112 = C_112 + (C_002*C_110 + 2*C_101*C_011)*rhoInv

    k_220 = C_220 + (C_200*C_020 + 2*C_110**2)*rhoInv
    k_202 = C_202 + (C_002*C_200 + 2*C_101**2)*rhoInv
    k_022 = C_022 + (C_020*C_002 + 2*C_011**2)*rhoInv

    k_122 = C_122 + (C_002*C_120 + C_020*C_120 + 4*C_011*C_111 + 2*(C_101*C_021 + C_110*C_012))*rhoInv
    k_212 = C_212 + (C_002*C_210 + C_200*C_210 + 4*C_101*C_111 + 2*(C_011*C_201 + C_110*C_102))*rhoInv
    k_221 = C_221 + (C_020*C_201 + C_200*C_201 + 4*C_110*C_111 + 2*(C_011*C_210 + C_101*C_120))*rhoInv

    k_222 = C_222 + (4*C_111**2 + C_200*C_022 + C_020*C_202 + C_002*C_220 + 4*(C_011*C_211 + C_101*C_121 + C_110*C_112) \
                     + 2*(C_120*C_102 + C_210*C_012 + C_201*C_021))*rhoInv \
                  - (16*C_110*C_101*C_011 + 4*(C_020*C_101**2 + C_200*C_011**2 + C_002*C_110**2) + 2*C_200*C_020*C_002)*rhoInv**2

    return ( C_000, C_001, C_002, C_010, C_011, C_012, C_020, C_021, k_022,
        C_100, C_101, C_102, C_110, C_111, k_112, C_120, k_121, k_122,
        C_200, C_201, k_202, C_210, k_211, k_212, k_220, k_221, k_222)
